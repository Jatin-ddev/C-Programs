#include <stdio.h>                            // For Printf() & Scanf()

int main()                                        // Starting Point of the program
{
  int a;                                           // Variable Declaration 


  printf("Enter a integer number\n");              // Request for input 
  scanf("%d", &a);                                   // Input from user 

  printf("You have Entered number %d\n", a);         // Printing on the output screen .

  return 0;    // End Status For Indicating that the program terminated successfully
}



//   %d , %i can be used to denote integer values 

