

//Program 6 Print Char Values From user

#include<stdio.h>                       // For Printf & Scanf()

int main(){                                 // Starting Point Of The Program 
    
    char a;                                     // Variable Declaration 
    
    printf(" Enter the Value : \n ");           // Request for Input 
    scanf(" %c", &a);                           // Input From User 
    
    printf("The Entered Value Is : %c", a);                     //Printing On The Output Screen 
    
    return 0;                                           // End status Indicating That the Program Terminated Successfully 
}


